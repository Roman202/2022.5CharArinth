
public class CharArithDemo {
	
	public static void main(String[] args) {
		char ch;
		
		ch = 'L';
		System.out.println("ch рівне " + ch);
		
		ch++;
		System.out.println("значення ch змінилось на " + ch);
		
		ch = 'D';
		ch--;
		System.out.println("значення ch знову змінилось і рівне " + ch);
	}

}
